DML stands for Data Manipulation Language, which is a subset of SQL that allows users to add, delete, modify, and retrieve data from database tables. DML is a crucial component of database management that helps users interact with data efficiently.


Here are some of the main DML statements:

INSERT: Adds new records to a database table 
UPDATE: Changes or updates existing data to a more recent value 
DELETE: Deletes values or data information from a table 


    INSERT INTO students (id, name, age) VALUES (1, 'John Doe', 20);

    UPDATE students SET age = 21 WHERE id = 1;

    DELETE FROM students WHERE id = 1;






