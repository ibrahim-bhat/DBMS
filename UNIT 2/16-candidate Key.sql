Finding Candidate Keys
A Candidate Key is a special set of attributes that can uniquely identify each record in a table. Think of it like your ID; itâ€™s what makes you uniquely you!

Example:
Imagine you have a student database with these attributes:

Student ID
Name
Email
To find a candidate key:

Look for Unique Identifiers: The Student ID is unique to each student (no two students can have the same ID).
Check Other Attributes: The Email might also be unique to each student.
So, both Student ID and Email are candidate keys because they can each uniquely identify a student.